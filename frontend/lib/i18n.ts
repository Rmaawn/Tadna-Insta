const DICTS: Record<Lang, Dict> = { fa, ar, en };

export function translate(
  lang: Lang,
  key: string,
  params?: Record<string, string | number>
): string {
  let s = DICTS[lang]?.[key] ?? DICTS.en[key] ?? key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      s = s.replace(`{${k}}`, String(v));
    }
  }
  return s;
}
